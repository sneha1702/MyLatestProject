package indexUtility;

public class IndexDoc {
	long itemLink;
	long itemTitle;
	long itemDesc;

	public long getItemLink() {
		return itemLink;
	}

	public void setItemLink(long itemLink) {
		this.itemLink = itemLink;
	}

	public long getItemTitle() {
		return itemTitle;
	}

	public void setItemTitle(long itemLink) {
		this.itemTitle = itemLink;
	}

	public long getItemDesc() {
		return itemDesc;
	}

	public void setItemDesc(long itemDesc) {
		this.itemDesc = itemDesc;
	}

}
